package bean;

/**
 * 公告
 * 
 * @author Administrator
 *
 */
public class Notice {
	private String href;
	private String title;
	private String wholeUrl;
	private String noticeBoardUrl;
	private String missionName;

	public String getHref() {
		return href;
	}

	public void setHref(String href) {
		this.href = href;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getWholeUrl() {
		return wholeUrl;
	}

	public void setWholeUrl(String wholeUrl) {
		this.wholeUrl = wholeUrl;
	}

	public String getNoticeBoardUrl() {
		return noticeBoardUrl;
	}

	public void setNoticeBoardUrl(String noticeBoardUrl) {
		this.noticeBoardUrl = noticeBoardUrl;
	}

	public String getMissionName() {
		return missionName;
	}

	public void setMissionName(String missionName) {
		this.missionName = missionName;
	}

}
